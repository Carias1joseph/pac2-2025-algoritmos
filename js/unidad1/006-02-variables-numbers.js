const numero1 = 12;
const numero2 = 15;

let result;

// Suma

result = numero1 + numero2;
console.log(`La suma de ${numero1} + ${numero2} = ${result}`);

// Resta
result = numero1 - numero2;
console.log(`La resta de ${numero1} - ${numero2} = ${result}`);


// Multiplicación

result = numero1 * numero2;
console.log(`La multiplicación de ${numero1} x ${numero2} = ${result}`);

// División

result = numero1 / numero2;
console.log(`La División de ${numero1} ÷ ${numero2} = ${result}`);











